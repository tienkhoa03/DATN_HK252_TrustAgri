/**
 * Icon Component
 * Hệ thống icon component với size variants
 * 
 * Requirements: 4.1-4.6, 8.4
 */

import React from 'react';
import { Icon as ZmpIcon } from 'zmp-ui';
import { iconSizes, navigationIcons, agricultureIcons, actionIcons } from '../../tokens/icons';

export type IconSize = keyof typeof iconSizes;
export type IconName = 
  | keyof typeof navigationIcons
  | keyof typeof agricultureIcons
  | keyof typeof actionIcons;

export interface IconProps {
  name: IconName;
  size?: IconSize;
  color?: string;
  className?: string;
  'aria-label'?: string;
}

/**
 * Icon component với size variants
 * Tích hợp Zaui icons và custom agriculture icons
 */
export const Icon: React.FC<IconProps> = ({
  name,
  size = 'md',
  color,
  className = '',
  'aria-label': ariaLabel,
}) => {
  const iconSize = iconSizes[size];
  
  // Determine icon source and name
  const allIcons = {
    ...navigationIcons,
    ...agricultureIcons,
    ...actionIcons,
  };
  
  const iconIdentifier = allIcons[name as keyof typeof allIcons];
  
  // Handle undefined icon
  if (!iconIdentifier) {
    console.warn(`Icon "${name}" not found in icon tokens`);
    return null;
  }
  
  const isZauiIcon = iconIdentifier.startsWith('zaui-icon-');
  const isCustomIcon = iconIdentifier.startsWith('custom-icon-');
  
  // Use custom SVG icons for all icons (navigation + agriculture)
  // This ensures consistent outline style across all icons
  if (isZauiIcon || isCustomIcon) {
    return (
      <CustomIcon
        name={name}
        size={iconSize}
        color={color}
        className={className}
        aria-label={ariaLabel || name}
      />
    );
  }
  
  return null;
};

